package mynavi2;

//配列

public class Array {

	public static void main(String[] args) {
		int[] no = new int [3];
		
		no[0] = 1;
		no[1] = 5;
		no[2] = 2;
		
		System.out.println("1番目のお客様、" + no[0] + "番目の窓口へ");
		System.out.println("2番目のお客様、" + no[1] + "番目の窓口へ");
		System.out.println("3番目のお客様、" + no[2] + "番目の窓口へ");
	}

}
